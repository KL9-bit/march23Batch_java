package control_statments;

public class Switch_Statament {

	public static void main(String[] args) {
		int day=2;
		
		switch (day)
		{
		case 1 :System.out.println("this is monday");
		break;
		
		case 2 :System.out.println("this is tuesday");
		break;
		
		case 3 :System.out.println("this is wensdey");
		break;
		case 4 :System.out.println("this is thurday");
		break;
		case 5 :System.out.println("this is friday");
		break;
		case 6 :System.out.println("this is saturday");
		break;
		case 7 :System.out.println("this is sunday");
		break;
		default:System.out.println("please enter valid data");
		break;
		
		
		}

	}
	}

