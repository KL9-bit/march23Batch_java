package loop_statement;

public class For_For_Stataement {
	//multiplication using "for" loops statement
		public static void main(String[] args) {
			for(int b=1;b<=10;b++)
			{
				
				System.out.println(2);
			}			
					
			
			System.out.println("=================================");
			for(int a=2 ; a<=20;a=a+2)
			{
				System.out.println(a );
				
				
			}
			System.out.println("=================================");
			
			
			for(int i=30;i>=3;i=i-3)
			{
				System.out.println(i);
			}
			System.out.println("=================================");
			//i want to print pune 5times
			for(int s=1;s<=5;s++)
			{
				System.out.println("Pune");
				
			}
		
		}
		

	
		
		
		

}
